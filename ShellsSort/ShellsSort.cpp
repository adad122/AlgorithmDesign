#include "stdafx.h"
#include <cmath>

void Output(int* datas, int length)
{
	for (int i = 0; i < length; ++i)
		printf("%d ", datas[i]);

	printf("\n");
}

void ShellsSort(int* datas, int length)
{
	int step = length / 2;
	while (step > 0)
	{
		for (int i = step; i < length; i++)
		{
			int n = datas[i];

			int j = i - step;

			while (j >= 0 && datas[j] > n)
			{
				datas[j + step] = datas[j];
				j-= step;
			}

			datas[j + step] = n;
		}

		step /= 2;
	}
}

